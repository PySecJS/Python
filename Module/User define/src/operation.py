def add(num1, num2):
    add = num1 + num2
    return {
        "Addition" : add
    }

def sub(num1, num2):
    sub = num1 - num2
    return {
        "Subtract" : sub,
    }

def mul(num1, num2):
    mul = num1 * num2
    return {
        "Multiple" : mul,
    }

def div(num1, num2):
    div = num1 / num2
    return {
        "Divide" : div
    }